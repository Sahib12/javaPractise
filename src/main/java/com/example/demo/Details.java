package com.example.demo;


public class Details {
	private String name;
	private String emailId;
	private String gender;
	private String age;
	private String pinCode;
	private String city;
	private String state;
	private String country;
	private String phoneNumber;
	private String programingSkills;
	private String tmay;

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getPinCode() {
		return pinCode;
	}
	public void setPinCode(String pinCode) {
		this.pinCode = pinCode;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getProgramingSkills() {
		return programingSkills;
	}
	public void setProgramingSkills(String programingSkills) {
		this.programingSkills = programingSkills;
	}
	public String getTmay() {
		return tmay;
	}
	public void setTmay(String tmay) {
		this.tmay = tmay;
	}
	
	
	
}
